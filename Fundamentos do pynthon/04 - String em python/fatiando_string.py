#Fatiamento de strings

nome = "Larissa Karoline Rodrigues da Silva"

print(nome[0])  # Primeira letra
print(nome[-1])  # Última letra
print(nome[0:7])  # Fatiamento do início até o índice 7
print(nome[8:])  # Fatiamento do índice 8 até o final
print(nome[::-1])  # Inverte a string
